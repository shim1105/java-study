package syntax;

public class AreaConditions {
	public static void main(String[] args) {
		int i=10;
		if(i==1) {
			System.out.println("i=1");
		}else if(i<10) {
			System.out.println("i>10");
		}else if(i>10){
			System.out.println("i<10");
		}else {
			System.out.println("i=10");
		}
	}
}
